package com.spring_boot.project.model;

public class SpecialtiesVO {
	private String speNo;
	private String speName;
	private String speDescript;
	private String regionName;
	public String getSpeNo() {
		return speNo;
	}
	public void setSpeNo(String speNo) {
		this.speNo = speNo;
	}
	public String getSpeName() {
		return speName;
	}
	public void setSpeName(String speName) {
		this.speName = speName;
	}
	public String getSpeDescript() {
		return speDescript;
	}
	public void setSpeDescript(String speDescript) {
		this.speDescript = speDescript;
	}
	public String getRegionName() {
		return regionName;
	}
	public void setRegionName(String regionName) {
		this.regionName = regionName;
	}
	
	
}
